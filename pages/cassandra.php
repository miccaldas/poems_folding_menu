          <p>These are the days of new beginnings,<br>
           and longing for the ones that are not our own anymore,<br>
           and wont be missed.</p>
          <p>These are the days of change and reinvention,<br>
           Of dead structure withering as dead leaves,<br>
           Of looking ahead to nothing known,<br>
           And living in the maelstrom of becoming.<br>
           Where we are just movement,<br>
           change,<br>
           and oblivion.</p>
          <p>Some call it decay,<br>
           Others birthing.<br>
           We call it pain and confusion.<br>
           We call it, our lives.</p>
          <p>These are the days of siren calls<br>
           Wailing their last warnings to deaf ears.<br>
           Everywhere we hear the Cassandra warning us about the demise of a life<br>
           That is already lost.<br>
           The death of the West,<br>
           Proclaimed for so long,<br>
           Is now an inexorable fact.<br>
           There are no ruins,<br>
           And there are no more men.<br>
           All the dreams are dead.<br>
           Sadder eyes and clearer heads are needed to live this becoming that engulfed us.<br>
           The past is no more a beacon,<br>
           As the future we’ve foreseen is no more there.</p><br>
          <p>///</p><br>
          <p>The plans of mice of men…<br>
           Are not plans but tropisms,<br>
           As men and mice are not agents but matter.<br>
           Nothing to do now but to contemplate the mill of history,<br>
           grinding away at today<br>
           Replacing it with a future that will frighten us all,<br>
           the Changing of the Guard generation,<br>
           or it won’t be any kind of future.</p>

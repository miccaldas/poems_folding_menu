          <p>I</ip><br>
          <p>Have they taught you nothing?<br>
           All the prophets,<br>
           all the poets,<br>
           All of them.<br>
           Thousands of years of words, deeds and actions,<br>
           Have you not heard?<br>
           Saw?<br>
           Understood?</p>
          <p>No matter<br>
           For what I’ve come to tell you is the summation of everything that was told before me.<br>
           At least everything true.<br>
           All truths, all science, all art, all philosophy,<br>
           All paths to freedom through knowledge,<br>
           come to this:</p>
          <p>It’s not important.<br>
           You are not important,<br>
           Your family is not important,<br>
           Your friends are meaningless,<br>
           Society does not exist,<br>
           There is no “we”,<br>
           Bury the fiction of Nation,<br>
           Of good deeds and bad,<br>
           Of knowledge,<br>
           Of beauty,<br>
           Of love<br>
           All of this is wrong, small and unimportant.<br>
           And it was this that they were telling you,<br>
           The prophets and poets and the others.<br>
           It was just this, they wanted you to know.<br>
           This is all you needed to know.</p>
          <br>
          <p>II</p>
          <br>
          <p>Rejoice, rejoice, rejoice!<br>
           For now you are free!<br>
           Rejoice, rejoice, rejoice!<br>
           For now you live in a brighter, better world!<br>
           All life, all of it,<br>
           in its complexity and abstruse variance,<br>
           is of no importance.<br>
           All has no meaning in this bigger picture will make our own,<br>
           For if it’s the result of evolving matter as say the scientists, what of it?<br>
           Theirs is but a small place in the universe,<br>
           Beautiful,<br>
           But small, and of no consequence.<br>
           And if life is the gift from Gods, is this the bigger boon we can imagine?<br>
           Surely not.<br>
           So rejoice, rejoice!<br>
           You were set free from respect to life!<br>
           And what about friends and family,<br>
           And all the bonds of empathy that keep us together?<br>
           What of them?<br>
           Since Life failed us as an answer,<br>
           we could always take refuge on the certainty of love.<br>
           But we all know,<br>
           deep down,<br>
           that this is not sure footing for the lovers of truth.<br>
           This is a marshy land, that shifts and moves ceaselessly.<br>
           Very beautiful and not to be trusted.<br>
           The answer is not love,<br>
           Nor is it all you need,<br>
           Because we need so much more.<br>
           Rejoice, Rejoice!<br>
           For the Ghost of love no longer haunts you!</p>
          <p>And the knowledge of all these things,<br>
           Brought together by reason, logic and intuition,<br>
           Is of no consequence even before its very eyes.<br>
           For its science itself that teaches us that what we know is but a speck in what there is to discover.<br>
           And that what we can imagine is like a haze covering what might be there.</p>
          <p>When you look at a stone,<br>
           any stone,<br>
           And imagine the millennia it witnessed:<br>
           Being on the bed of a lost sea,<br>
           Having risen to heights of mountains that disappeared,<br>
           watching dead animals howling at stars that<br>
           for so long<br>
           have changed their place in the skies…<br>
           To now be carried unaware in your hand,<br>
           with no inkling of itself,<br>
           deaf, dumb and blind.<br>
           This in each and every rock!<br>
           Rejoice!<br>
           The burden of knowing was lifted from your shoulders!</p>
          <p>And God in all this? Where to find him?<br>
           Everywhere.<br>
           For this is a poem not about the death of God but of its awakening.<br>
           Not the God of the spiritual accountants they keep chained in Churches,<br>
           Any church,<br>
           Not the personal God who has hygiene advice’s for you,<br>
           Your wife,<br>
           Your dog.<br>
           Not the God of everlasting love and goodness that sounds so human that we call him Father,<br>
           sometimes,<br>
           Mother,<br>
           More often,<br>
           creating sacred families of variable geometries,<br>
           and unfailing dysfunctionalitiy.<br>
           God is so,<br>
           so,<br>
           distant.<br>
           Beyond the last frontier of imagination.<br>
           Making sure by its presence that our wildest dreams are nothing but small extensions of ourselves,<br>
           Not the yardstick with which to measure the Universe.<br>
           So rejoice!<br>
           For God is far way!</p>
          <p>Haven’t you learned nothing?<br>
           From all the Poets, Philosophers and Prophets of the ages?<br>
           To think far and wide and deep,<br>
           so you can see how near and small and shallow you are?<br>
           This was all the knowledge you needed to live free from fear.<br>
           And even this,<br>
           Is not important.</p>

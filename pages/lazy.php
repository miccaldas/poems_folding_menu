          <p>Lazy afternoons,<br>
           all made of sunshine and permanence,<br>
           light and duration;<br>
           could they last forever…<br>
           Listless Sun soaked hours,<br>
           filled to the brim with nothing<br>
           and hollowed of any promise but its own shiny pointlessness.<br>
           These are the times that are,<br>
           truly,<br>
           ours.<br>
           This is our allotted time,<br>
           all else is purposeful living.<br>
           Years, months and days dedicated to the pursuits that we esteem as worthy:<br>
           career,<br>
           money,<br>
           the respect of our peers,<br>
           more money<br>
           ourselves,<br>
           family,<br>
           the masses,<br>
           God,<br>
           the whales.<br>
           All these and more<br>
           much more,<br>
           occupies us inside and out,<br>
           as we dedicate large chunks of our lives to their conquest.<br>
           Noble and important as they may be,<br>
           (they’re not)<br>
           they are not truly ours.<br>
           Their place is the no man’s land that separates what they want from you,<br>
           from what you want from them.<br>
           Its in this field of glory that you’ll forget yourself, to win them,<br>
           and where they’ll accommodate you, for no greater good.<br>
           Everybody gives a little,<br>
           nobody wins.<br>
           This is the base of the new social contract,<br>
           a fierce and egalitarian force preventing anyone from getting what they want.<br>
           Social justice making sure that everyone gets fucked the same<br>
           and in the same way.<br>
           A socialization of misery,<br>
           if you will.<br>
           But not today.<br>
           at least not this afternoon.<br>
           Today there’s nothing to do but be,<br>
           here.<br>
           Useless,<br>
           and with nowhere to go.</p>

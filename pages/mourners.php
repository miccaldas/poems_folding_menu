          <p>Mornings are for mourners, To mourn the night before.</p>
          <p>Morning is Night’s final throes of desperation.</p>
          <p>And in her place…</p>
          <p>Nothing;</p>
          <p>nothing but the cooling corpse of yesterday.</p>
          <p>You open your eyes , sit on the bed (still unsure of where, how and why you’re here) thinking what to do with the sprawling remains extended till noon.</p>
          <p>You await for the new day</p>
          <p>the new day</p>
          <p>to come and rescue you from this queasy unsureness that is morning.</p>
          <p>But no.</p>
          <p>Oh no.</p>
          <p>You’ll inhabit this no man’s land till the afternoon.</p>
          <p>Morning the death shrine of last night.</p>
          <p>All promises , all possibilities</p>
          <p>growing in the hopeful shades of dark,</p>
          <p>Fizzled to nothingness in the raw light of the new day…</p>
          <p>Crumpled little ashen mounds of wanting,</p>
          <p>burnt by the ugliness of time.</p>
          <p>And a dense cloud of unhappiness clings to everything and everyone as if t’was the Sun royal mantle.</p>
          <p>Yellow and bloated;</p>
          <p>Smilingly pontificating over the debris of the new day.</p>
          <p>Another day.</p>

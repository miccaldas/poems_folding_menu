          <p>“I’m done with drinking!” said the glass.</p>
          <p>“I’m done with the loud bars and the loud hearts, with bragging souls and fighting spirits.</p>
          <p>Done with the coming and the going of wayward travelers of the late hours…</p>
          <p>I’m done with the night!”</p>
          <p>Said the glass almost blushing… awkwardly masquerading his unease with a smile. He paused, gathering himself, and then he continued, as if reading from a book,</p>
          <p>“I’m done with the silky invitation of the dark and the smiling complacency of the moon. I’m done with all this wanting and all this being and all this aimless meandering. cruising blind and fast, hither to thither, from here to there, from there to here again, and then to nowhere.”</p>
          <p>“I’m through with nights, I’m through with days that are still nights, and I’m through with days you wished were still nights!”</p>
          <p>“I’m through.” Pondered the glass…</p>
          <p>“I’m through being half filled and half empty, I’m through being drunk , spit upon, cried over, spilled and left on the ground… forgotten.”</p>
          <p>…</p>
          <p>“All the things I could be useful for… all things that I can do for me and others, all these things, all good. Are nothing to me.”</p>
          <p>“all the things that I am, all the things that make me, me… are things where I see me no more.”</p>
          <p>“There is no more of “me” that I can do. I have become weary of existing. I’ve grown unaccustomed to being, I’m a stranger to living… none of this is mine anymore.”</p>
          <p>“All that I am is elsewhere, differently, in places unseen, and probably, in moments unwanted.”</p>
          <p>“I am now, whole. an enormous longing for being someone else.”</p>
          <p>“Today I’m the cessation of volition, the end of habit and the answer to the algorithm of my own demise.”</p>
          <p>“And, knowing this, knowing only this.”</p>
          <p>“I wait that the Great Tyrant Time pushes all my doubts backwards into the past. So I can be whole, alone, With what I am… now.”</p>

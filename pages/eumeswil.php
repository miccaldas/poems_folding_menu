          <p>When I close my eyes I see a desert.</p>
          <p>The, long, brown, infinity that stretches from the mountains to the sea.<br>
           Leaving everything in between dead, and indifferent to its embrace.<br>
           A space of immobility where movement is Wind.<br>
           Blowing.<br>
           Hard and soft….<br>
           Subtle and strong…<br>
           Northward and southward,<br>
           Hither and thither.<br>
           Striping the structure out of the desert.</p>
          <p>The only truth the wind.<br>
           And the wind is the desert.<br>
           And the desert is …<br>
           It.</p>
          <p>The desert as the end of wanting.<br>
           The last mile of hope in the road to despair.<br>
           Where the Saviour goes to die.<br>
           This is the place were everything stops, and is moved by chance.<br>
           The place of the prime movement<br>
           Fate,<br>
           Chance,<br>
           Flow.</p>
          <p>When I close my eyes I see a desert,<br>
           Beneath me, and in front of me.<br>
           In space in now, in time in here.<br>
           Southward on the other side of the sea,<br>
           in the other side of the ocean.<br>
           Europa’s birthplace.<br>
           My birthplace.<br>
           The desert that was then when we started.<br>
           When Europa made its first tentative steps to Itself.<br>
           A desert not as a desert but as a barren place;<br>
           A place where to be free.<br>
           In the Future,<br>
           Here,<br>
           In Europa,<br>
           The Peninsula will be a undulating vastness of naught.<br>
           Rocks and sand where once stood the cities of today,<br>
           Ruins of fountains, pools and waters mirrors,<br>
           Incomprehensible artifacts of a past best forgotten.<br>
           And,<br>
           Scurrying nimbly through the desert,<br>
           Our children’s children.<br>
           Trapped in a culture too slow to move with time.<br>
           Still believing themselves Empire,<br>
           but,<br>
           now,<br>
           all made of sand.</p>
          <p>Yesterday,<br>
           Late in the day,<br>
           Yesterday,<br>
           Long ago,<br>
           In a day dead and done,<br>
           The day before now,<br>
           Yesterday.<br>
           It’s hours frozen by time,<br>
           For it’s yesterday.<br>
           Final and frail as porcelain dolls.<br>
           These are the days that you’re given.<br>
           Existing because time is locked in yesterday.<br>
           Yesterday where knowledge goes to die.<br>
           A land of reason and language,<br>
           The last sarcophagi of living and a prison.<br>
           It’s in this tomb of being that I see the desert,<br>
           Southward and in the desert.<br>
           A premonition of nothing,<br>
           A call to arms<br>
           And words to the wise</p>
          <p>The sky seems to get a little bit higher,<br>
           Going upwards and upwards,<br>
           Escaping us in a hurry.<br>
           Fleeing a place promised to anathema.<br>
           Knowing something we don’t.<br>
           The rain is evermore scarce,<br>
           The forests crackle dry and burn in winter.<br>
           The rivers turn into torrents of mud.<br>
           Crocodiles hunt in the mountains<br>
           And bears fish by the sea shore.<br>
           The climate is changing and we seem to stand still.<br>
           As the oceans flood the lands<br>
           And the desert gobbles up forests.<br>
           Rain now only a wet remembrance of days gone by<br>
           An enormous sadness for the watery world we left behind,<br>
           Fills the air under a indifferent sky.<br>
           Growing evermore remote.<br>
           Scientists say it’s not going to rain anymore.<br>
           That we shouldn’t count on our crops,<br>
           Anymore.<br>
           Rain buried in the dust that becomes wind,<br>
           Flying through the ocean to the,<br>
           Original desert.<br>
           Sand as the seed of the future.<br>
           Feeding on each other,<br>
           And turning the Mediterranean a salty oasis.</p>
          <p>Scientists say our land is dying and that we should move away,<br>
           Elsewhere,<br>
           Anywhere,<br>
           To a land that’ll keep us…<br>
           But I … I’m not a scientist,<br>
           So I’ll think I stay.<br>
           And as I prepare to wait I ask myself,<br>
           Will the desert bring its own religion<br>
           Will this be the final death of the Green Man?<br>
           The Green Man survived the cross,<br>
           Will he survive the death of the woods?<br>
           Will the sand bring forth Mohamed or will this be Christ greatest triumph?<br>
           He is,<br>
           After all<br>
           A son of the desert,<br>
           Or,<br>
           Maybe,<br>
           In the end,<br>
           This will be the final moment in a 2000 year old changing of the guard.<br>
           Perhaps Mohamed and Christ are not all that different,<br>
           Both are sons of the desert and grew feeding on the spirits of the wind,<br>
           The tree spirits mortal enemies.<br>
           Or maybe something new will appear,<br>
           A culling of all this deities, places and times.<br>
           A crucible to bring forth a new and happy science,<br>
           But,<br>
           This is not a place of beginnings but of endings.<br>
           A place best understood by the old at heart,<br>
           The cautious and suspicious mood of age best suits this changing mores.<br>
           And the old,<br>
           Out of habit and out of fear,<br>
           Look to the past for answers.<br>
           To yesterday,<br>
           To the house of porcelain dolls of intelligence and discourse.<br>
           But not even time past can illuminate the somber path that lies ahead.<br>
           Only looking long into the past,<br>
           Can we glimmer faintly to something akin.<br>
           Deep into the harbour of the first wanting,<br>
           can we find the face that suits these final features.</p>
          <p>Europa’s long journey from the bogs of magic though the plains of philosophy and to the rarefied mountains of science<br>
           has brought to a place so bereft of everything else,<br>
           That only the first Gods<br>
           Who were not here and needed nothing,<br>
           Can exist<br>
           Finally liberated from the shackles of religion,<br>
           The bonds of reason<br>
           The yoke of aesthetics<br>
           The burden of morality<br>
           We were delivered free<br>
           To the desert of our own creation.<br>
           And as form follow function<br>
           And matter follows spirit,<br>
           Our land becomes as inhabitable as our heads.</p>
          <p>When I close my eyes I see a desert.<br>
           I move from the window where I stood,<br>
           And stagger into my darkened bedroom,<br>
           And lying on my bed,<br>
           Eyes wide,<br>
           Looking intently into the ceiling<br>
           I shed the first tears for the place where I’ll live, Waiting its coming, Their people.</p>

          <p>Stained clouds by the oily sludge of the night<br>
           mar the skyline who looms over the alley and dinner,<br>
           Twitching, squirming,<br>
           Bloody and delicious.</p>
          <p>I watched as it moved one last time.<br>
           The moon cowered beneath the smog,<br>
           Neon sprayed light trough the dark recesses of the city…<br>
           Low, dwindling stars, illuminate a mankind that deserves no other light.</p>
          <p>I lean against the wall, breathless and sated,<br>
           Casting my eyes to on high,<br>
           Searching for a God that has forsaken me.<br>
           Leaving me alone with the billboards of banks and travel agencies,<br>
           Sneakers and massage parlours,<br>
           Shoe stores and chemists…<br>
           Brightly coloured hues of dreaming, irradiating over the street.</p>
          <p>Full of the night lights and city sounds…<br>
           Resting and digesting,<br>
           as if the corpse before me was not my doing.<br>
           As if no one would,<br>
           ever,<br>
           Come here<br>
           As if I could rest …<br>
           for awhile.</p>
          <p>The food stopped moving long ago.<br>
           An empty dish for a vacant meal.<br>
           The thrill of the hunt, the pleasure of the kill,<br>
           Feeding…<br>
           All so quick, so good and so sudden.</p>
          <p>I get up and get ready to leave,<br>
           Button up my coat and leave this happy place of feeding.<br>
           Strangely nostalgic for this now that is ended.<br>
           The cold wind sharpens my pace as I look for the train station.<br>
           Hungry to be elsewhere.<br>
           Away from this geometric unwelcoming town.<br>
           Eager, so eager,<br>
           To get home.<br>
           Away from all the animals that roam these streets.</p>

          <p>Will we weep for the loss of absent friends?<br>
           Will time leave us alone?<br>
           Will sloth and neglect, estrange us from the ones we love the most?<br>
           Will the mill of days grind away the ties that bound us?</p>
          <p>No time like now,<br>
           No words but these words<br>
           No persons but these persons…<br>
           The ones here.<br>
           Not the chosen,<br>
           But the remaining.</p>
          <p>But<br>
           I ask myself…<br>
           Will we forgive ourselves for our forgetfulness?<//p><br>
          <p>///</p>
          <p>All people<br>
           Everywhere<br>
           Everyday…<br>
           The enormous throng of people we knew and loved…<br>
           Taken by time and sacrificed in the altar of now.<br>
           Distant shadows of a past made unnecessary.<br>
           Not by you.<br>
           Not by the others.<br>
           But by now,<br>
           By the movement.<br>
           This place in this time, and this place in this<br>
           place.<br>
           Love trampled under the wheels of necessity<br>
           and the stupid fascism of need.</p>
          <p>And to all this I ask:<br>
           Will we miss our friends forgotten?<br>
           Will we need their love and warmth?<br>
           Will we be lonely?<br>
           Afraid of being adrift with ourselves?</p><br>
          <p>///</p><br>
          <p>Time passes like a glacier trough a valley,<br>
           Slowly but surely, all will collapse under the weight of the frost.<br>
           Like the valley,<br>
           We’ll be striped of everything,<br>
           Naked and alone.<br>
           And here, in presence of our own structure,<br>
           Before the great truth…<br>
           We cowardly long for time we spent with others and the truths we shared.<br>
           Longing for all the things we left behind…</p>
          <p>And,<br>
           Knowing this…<br>
           Will we weep for the loss of absent friends?</p>

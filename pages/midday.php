          <p>“Seek and ye shall find.”<br>
           Says the Good Book.<br>
           Many were lost for heeding these words.<br>
           For if there are plenty of books,<br>
           Not one,<br>
           is Good.</p>
           <p>But still they manage to get written,<br>
           engraving themselves in the hearts of men who bask by the fire of Words,<br>
           and,<br>
           foolishly,<br>
           look for their reflection in the World.</p>
           <p>These are the starry eyed dreamers,<br>
           The heroes,<br>
           The revolutionaries…<br>
           drowning in contemplation of the Palace of Reason that exists only in the soul…<br>
           not the ever decaying chaos of becoming.<br>
           They’re blinded by truth,<br>
           drunk on certainty,<br>
           choking on Reality,<br>
           and gagging in Light…<br>
           They look to the world in hope..<br>
           waiting for it to turn out as they know it should.<br>
           And,<br>
           Seeing it carrying on indifferent and unaware of their plans.<br>
           They take on themselves the burden of change.<br>
           And so they walk;<br>
           The path of the good man,<br>
           The path of the righteous and the pious…<br>
           Of the northern man,<br>
           cowering under dreams of land and bloodlines…<br>
           The path of the Cow.</p>
           <p>Oblivious to everything but of the small pasture his herd calls home,<br>
           Wanting for nothing and thinking about less.<br>
           And as they gather to celebrate their bovine ethos of exclusion, we hear their rambling ruminations on family,<br>
           land,<br>
           purity.<br>
           Celebrating closeness and sameness,<br>
           Making of their hearth their world.</p>
           <p>But,<br>
           As we avert our eyes from this cozy landlocked scene,<br>
           and turn to the vast sea that faces us, each an every day,<br>
           We cannot help but think that,<br>
           Not for us the clear trek,<br>
           Not for us the known path…<br>
           We’re of the Midday,<br>
           We’re made of hard truths and harder lies…<br>
           Here,<br>
           baking beneath the inflexible sun,<br>
           lingers a race of dark men,<br>
           Who live under the terrible burden of the naked truth.<br>
           No wolf gods, no mists no trees, no rivers, no fountains no nothing!<br>
           But the unavoidable burden of living alone before the Great Power.<br>
           Distant, cold and aloof.</p>
           <p>A God not for the man who needs prayers, sacrifices, dances, parties, feasts, drugs…<br>
           All the paraphernalia that lesser man use to cloud the unknowing.<br>
           Here<br>
           Beneath the high blue skies and the rampant violence of the sun,<br>
           Loiter those for whom God is so present that it hurts.<br>
           A belief so deep that is not a belief anymore,<br>
           But life devoid of pleasure and contentment.<br>
           Empty of everything but His intrusive existence.</p>

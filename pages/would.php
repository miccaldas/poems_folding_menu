          <p>Would if I could, refuse me from life and its appointments.</p>
          <p>If I could, I would, sever all ties with polite society, set sail from humanity,</p>
          <p>Shun all morality, all knowledge, all Love.</p>
          <p>Not out of rebelliousness, and least of all to be free,</p>
          <p>but out of sloth.</p>
          <p>The apathy that impedes me from acting has led to this philosophical detachment. That’s not philosophical at all.</p>
          <p>It is, whole, inertia.</p>
          <p>Would if I could, surrender unashamed to the weariness brought by the passing of time. Quietly address the silence that’s inside,</p>
          <p>wrap me up in indifference and leave the world to its own devices.</p>
          <p>So I could watch TV… for eons and eons.</p>
          <p>My indolence is irreducible to logic and cognition.</p>
          <p>There’s no causation for this sadness, There’s no sickness or sorrow that could explain it. It’s just there, like gravity… one and the other bringing everything we know crashing into the ground …</p>
          <p>My mind is one with the forces of the universe, fighting nothing and wanting for less,</p>
          <p>I wait that the last guests leave so I can be alone.</p>
          <p>But I know, acutely, that talking of being one with the universe is nothing but words in love with words taking them to the end of themselves, finding release on the cusp of nothing .</p>
          <p>Laziness, pure and simple.</p>
          <p>And as I sit here writing these words, I’m reminded of younger days…</p>
          <p>Of watching adults go through theirs lives half awake, responsive to just a small set of stimuli, Living a life that the dead would be ashamed of living.</p>
          <p>well… I believe I achieved maturity. I am an adult… And not even the horrified gaze of a younger me will wake me from this stupor.</p>
          <p>I also remember reading that when a man has children looses half his soul.</p>
          <p>Forever incapable of redemption, an empty shell, living a life that the dead would be ashamed of living. well… I’m a father now. She’s three and she’s adorable. Do you want to see pictures?</p>
          <p>But all this remembering feels like reenacting finished conversations. Lines of inquiry who were closed because conclusions were found already. All this knowledge is useless and cold.</p>
          <p>Even if it is the only source of explanation for me…</p>
          <p>but… honestly, I can’t be bothered with myself.</p>
          <p>II</p>
          <p>Would if I could… but I cant.</p>
          <p>And perhaps its better this way, leaving even the wish of ataraxia unfulfilled…</p>
          <p>what better monument to anomie but the failure to stop. There’s a deep logic in a laziness so profound that stills acts.</p>
          <p>But, still, who cares? who could possibly care? why?</p>
          <p>God knows I don’t.</p>

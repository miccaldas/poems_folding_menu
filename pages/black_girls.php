          <p>Black girls peering over the steeple… Silent disbelief as the monks looked at dune where the women stood. The slow, red, caressing wind, touched their faces, hair, and feet, spreading incredulity all trough the desert.</p>
          <p>The day was getting late. The hill was ahead… high and long… Northern breeze blowing the sand out of the buried church.</p>
          <p>The women looked at the monks; looked at the sky and; went away… over the hill and nowhere to be seen.</p>
          <p>The night drawed near and colder. Paulus, Josephus, Silva, stood tired and silent before the hill of sand that surrounded the church. The Old church. The church they longed to find.</p>
          <p>It grew darker and darker, colder and colder… Night oozed blackness through the golden stony desert… Everywhere sand, stones and nothing.</p>
          <p>They walked for 40 days to get here… And now; for the first time in 3 days.</p>
          <p>They stopped. Tired and unable to move. Parched and gasping for breath. Weary and kneeling in front of the dune.</p>
          <p>Before it they stood; breathless, Their legs broke, their eyes widened, their hearts bleeding. There were no more places to go and no more prayers to pray. Prostrated, broken, waiting.</p>
          <p>The sunset came over the desert like a sunset who comes over the desert, as men waited, like men wait in the desert that will kill them.</p>
          <p>Looking at the dune that’s not a dune but a hill, and hill that’s not a hill but a church. The Old church.</p>
          <p>Clearer and clearer through the night wind… it’s stony walls cleansed by the dust of passing time. Long, Long, Time ago.</p>
          <p>Far, Far, away,</p>
          <p>in the desert. There were three names who were men, and men who were monks, and the monks who remembered their master… He spoke of the new God. The Man-God of Galilee. Chocked like a beggar, died like a king. He brought them news of the end of the old gods and the promise of a new beginning.</p>
          <p>Servants of Jesus, the Christ. Fishers of men; Fishing for spirit beneath the remains of the flesh.</p>
          <p>Their zeal and piety was not enough. The light of God trembled dimly through the haze of doubt and reason…</p>
          <p>And the longing for the Nazarene became a wanting, and the wanting became a journey; and the journey became a story; and the story was this:</p>
          <p>When the Verb Incarnate walked the earth, he searched the desert, listening for the fading sounds of God. Deeper and deeper into the vast white silence, into the barren loquatorium of the Godhead.</p>
          <p>And, He found himself very, very far, far, away from everything else.</p>
          <p>He saw for the first time his own Divinity; Naked and triumphant; the presence of Being as a wave of destruction engulfing this unwelcoming altar. His life and body a fitting sacrifice for the reward of the final truth… In this place, In the center, the middle. Inside, him.</p>
          <p>This was the first church. This was the place of the prime sacrifice. Where Jesus died for Christ, and the destiny of the world was sealed by the Son of Man.</p>
          <p>It’s this empty place of giving the monks searched for… walking, not standing… looking, not finding… living, not being, in the centre of God’s first sacrifice to himself.</p>
          <p>And, Lo, They found it in the desert.</p>
          <p>Under the hill, the dune, the sand, time,</p>
          <p>darkness made flesh.</p>
          <p>On their knees, before the night wind, blowing the sand from the church that awaited them.</p>
          <p>And, as they waited, horrified, terrified… as God slowly striped before their unwanting eyes, they watched, (after the point of no return) the grim spectacle of men made spirit, and spirit made sacrifice.</p>

          <p>I</p><br>
          <p>All people have a geography.<br>
           Even if beggared from anything else,<br>
           they exist in space,<br>
           and their pattern in it<br>
           is wholly their own.<br>
           We all have a topology,<br>
           although there might be nothing to distinguish us,<br>
           although we may be silent in history,<br>
           our paths exist,<br>
           although known only to ourselves.</p>
          <p>Today I make this pronouncement<br>
           That all paths of men are to be studied.<br>
           That all geographies are important,<br>
           and I suggest here,<br>
           for the very first time.<br>
           the hypothetical existence,<br>
           never seen because never studied,<br>
           Of a trek that,<br>
           If lacking in istory,<br>
           overflows in topography.</p>
          <p>Routines made of subways, trains and trams crossing lines in the skin of the city,<br>
           mandalas built of boozy, bar hopping nights,<br>
           sigils born from trips to the drugstore in the corner,<br>
           and cosmogonies explained by holidays at the beach.</p><br>
          <p>II</p><br>
          <p>Yesterday I dreamt of a girl I loved at eleven.<br>
           And in the dream I saw her, now.<br>
           Older, thinner … a little gaunt,<br>
           But still beautiful.<br>
           Immense brown eyes<br>
           attentive,<br>
           restless,<br>
           and tense.<br>
           And I,<br>
           I loved her with the intensity of a eleven year old.<br>
           I was elated by this gift of feeling love like the first,<br>
           for a second time.</p>
          <p>I found her in a small town<br>
           In the dream<br>
           And after she disappeared saying very little,<br>
           I knew that that was where I would live forever.</p>
          <p>Then I dreamt about her friends,<br>
           the town,<br>
           and how to stay there.<br>
           In the dream they asked me if I,<br>
           really,<br>
           wanted to stay in the town.<br>
           Which was<br>
           In fact<br>
           So small.<br>
           And I said<br>
           “Yes.<br>
           She’s the love of my life.”</p><br>
          <p>III</p><br>
          <p>There’s a concept called “path dependence”, that says that your current decisions are determined by earlier ones.<br>
           That all that happens results from what you did, no matter how remote,<br>
           and that the past is the solution to the present.<br>
           In this world there are no master plans,<br>
           no strategic visions,<br>
           guiding lines,<br>
           No.<br>
           In this way of seeing, all that there is, is, because of previous actions.<br>
           No other truth but the path,<br>
           steps taken,<br>
           nothing else.<br>
           In this vision the route is all.<br>
           It’s the cause and the explanation,<br>
           the need and the answer.<br>
           And maybe it’s for this that all geographies are important.<br>
           Because there’s no teleonomy<br>
           teleology,<br>
           eschatology,<br>
           to illuminate the path that is us.</p>
          <p>And here we stand,<br>
           In our own timelines<br>
           frail and complete,<br>
           Only in them, ontology<br>
           Only in them, aesthetics,<br>
           Only in them, the hope of redemption.<br>
           And for all this,<br>
           And because of this,<br>
           a man,<br>
           at 44,<br>
           can dream about the beginning of his life,<br>
           and wake up the next morning,<br>
           happy,<br>
           with a heart full of songs.</p>

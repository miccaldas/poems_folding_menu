          <p>"-You have to visit your grandmother. She’s weak now and won’t be with us for very long.”</p>
          <p>Said my father, while driving us in that December night.</p>
          <p>I mumbled noncommittally and looked at the trees outside.</p>
          <p>“She became frailer this last few months.</p>
          <p>You haven’t seen her for so long, so you don’t know.</p>
          <p>She looks like a little old lady.”</p>
          <p>Which was something strange to say about a 85 year old woman.</p>
          <p>But not so strange if you knew her.</p>
          <p>upright</p>
          <p>uptight</p>
          <p>unmoving</p>
          <p>unwavering</p>
          <p>unshakable</p>
          <p>wholly unlikable.</p>
          <br>
          <p>////</p>
          <br>
          <p>When we were evicted and my parents divorced,</p>
          <p>because when it rains, it pours,</p>
          <p>me and my sister went to live with my grandmother.</p>
          <p>much to the chagrin of everyone involved.</p>
          <p>But mostly hers.</p>
          <p>Our time there was one of abuse, spite and confrontation,</p>
          <p>and waking to the knowledge that the ones that should protect,</p>
          <p>sometimes bite.</p>
          <p>We were told that her house was not like our parents house,</p>
          <p>that, in her house there were rules,</p>
          <p>order,</p>
          <p>and that, if we abided by them, all would be fine.</p>
          <p>But this was a lie.</p>
          <p>The rules were not there to rule the house,</p>
          <p>but to change our behaviour.</p>
          <p>which was seen as undisciplined</p>
          <p>moor less</p>
          <p>unfocused</p>
          <p>weak.</p>
          <p>All free time was an opportunity for sloth,</p>
          <p>our interests were suspicious as they were chosen by us,</p>
          <p>our achievements were not to be celebrated so we wouldn’t think too much of ourselves.</p>
          <p>our parents were feeble, stupid,</p>
          <p>And we should be grateful we didn’t live with them anymore.</p>
          <p>This was our original sin,</p>
          <p>the indiscipline of our parents,</p>
          <p>and thus,</p>
          <p>we were the result of bad stock and bad habits.</p>
          <p>My father lived with us,</p>
          <p>she was his mother,</p>
          <p>and tried his damnedest to see and hear nothing.</p>
          <p>He resigned from fatherhood and became a son.</p>
          <p>expecting cooked meals,</p>
          <p>pressed clothes,</p>
          <p>clean shirts</p>
          <p>and no sons to disturb this second adolescence.</p>
          <p>I hated him so much …</p>
          <p>my sister still does.</p>
          <p>But what I hated most of all was conceding to my grandmother</p>
          <p>that he was,</p>
          <p>indeed,</p>
          <p>a coward.</p>
          <p>Lost in himself.</p>
          <p>forever blameless because forever fourteen,</p>
          <p>an hedonist not out of love of life,</p>
          <p>but because little boys don’t make plans.</p>
          <p>Living there was a war that made me defensive and wary of people for years.</p>
          <p>Cagey, afraid and suspicious of kindness.</p>
          <p>Always on guard,</p>
          <p>ready to bite and run</p>
          <p>always alone and happy to be so.</p>
          <p>When my mother got a rented room I went to live with her,</p>
          <p>sleeping on the floor next to her bed.</p>
          <p>As I was going out the door my grandmother touched me on the shoulder and,</p>
          <p>as I turned,</p>
          <p>slapped me twice on the cheeks.</p>
          <p>“I know why you’re going away! So you would do what you want! Go be a failure like your father!”</p>
          <p>Her eyes were wide,</p>
          <p>hands taut and claw like.</p>
          <p>She seemed eager to pounce.</p>
          <p>I was twelve years old.</p><br>
          <p>////</p><br>
          <p>And then I didn’t see her for years.</p>
          <p>My father would try to get me to come to her house,</p>
          <p>remembering me that she loved me,</p>
          <p>saying that all that I remembered,</p>
          <p>all that happened,</p>
          <p>was nothing more than lazy teens living with an old lady set in her ways,</p>
          <p>Just that.</p>
          <p>That,</p>
          <p>sometime in the future,</p>
          <p>I would look back and laugh at these cherished family memories.</p>
          <p>And,</p>
          <p>furthermore</p>
          <p>I probably,should have heard some of the things she said to me,</p>
          <p>as they were advice to be heeded.</p>
          <p>Many years passed,</p>
          <p>ten, maybe more.</p>
          <p>I was in the car, with my father, on that December night.</p>
          <p>I think it was my sister’s birthday and we were all going out to dinner.</p>
          <p>My father roped me to go pick my grandmother.</p>
          <p>and I …</p>
          <p>by then,</p>
          <p>didn’t feel much of anything.</p>
          <p>Ten years passed,</p>
          <p>everything fades to gray.</p>
          <p>I remember it was cold.</p>
          <p>The car heater was on and I had my overcoat,</p>
          <p>but, if I pressed my face to the window,</p>
          <p>I could feel the sting.</p>
          <p>We parked outside her house, and he said:</p>
          <p>“Go get your grandmother. She’s waiting for us.”</p>
          <p>When I opened her door, a numbing chill came from inside.</p>
          <p>the stale dusty winter of closed houses.</p>
          <p>All the lights were out and I asked myself if she was sleeping,</p>
          <p>or dead.</p>
          <p>I called:</p>
          <p>“Grandma! … Grandma! It’s Miguel Grandma!”</p>
          <p>Silence.</p>
          <p>I climbed the stairs,</p>
          <p>my steps muffled by the garish carpet she liked,</p>
          <p>dark</p>
          <p>unmoving.</p>
          <p>As I got up there I heard:</p>
          <p>“Oh, it’s you. I thought that your father would pick me up.”</p>
          <p>There she was.</p>
          <p>Bent over on an unforgiving chair that was never used to sit, she sat.</p>
          <p>In the dark,</p>
          <p>by an open window,</p>
          <p>letting in the street lights dim glimmer.</p>
          <p>She looked frailer,</p>
          <p>smaller,</p>
          <p>distant.</p>
          <p>“Grandma aren’t you cold?”</p>
          <p>“Cold? No, not really. Why? Is it cold today?”</p>
          <p>“Why are you sitting in the dark?”</p>
          <p>“… well, I was alone here, I wasn’t reading … I saw no reason to waste electricity”</p>
          <p>She looked vaguely at me,</p>
          <p>as if it should be mine the next move,</p>
          <p>as if she needed a prompt.</p>
          <p>“Come on Grandma, I’ll help you get up”.</p>
          <p>I took her arm,</p>
          <p>she took her cane,</p>
          <p>which I never seen her use,</p>
          <p>and, as she leaned on me, I felt the weight of nothing on my arm.</p>
          <p>She was insubstantial</p>
          <p>indifferent to the cold</p>
          <p>and not really there.</p>
          <p>“I thought your father would pick me up.”</p>
          <p>She said again.</p>
          <p>“He’s waiting in the car Grandma”</p>
          <p>“Oh … OK”</p>
          <p>…</p>
          <p>“Where are we going again?”</p>
          <p>“It’s Margarida’s birthday Grandma. We’re going to dinner.”</p>
          <p>“Oh … OK”</p>
          <p>Late that night we came back from Lisbon,</p>
          <p>through the “Marginal”,</p>
          <p>winding from Lisbon,</p>
          <p>to Cascais,</p>
          <p>always hugging the sea.</p>
          <p>My grandmother looked outside and,</p>
          <p>for the first time that night,</p>
          <p>she looked engaged.</p>
          <p>“This is so pretty…</p>
          <p>Maria.”</p>
          <p>Her eldest sister.</p>
          <p>“Would love to see this…</p>
          <p>I’ll tell her tomorrow.”</p>
          <p>Maria had died ten years ago.</p>
          <p>“Zé!”</p>
          <p>My father</p>
          <p>“Do you see the lighthouse?!”</p>
          <p>“Yes mother, I do.”</p>
          <p>“So pretty…</p>
          <p>I’ll tell your father.”</p>
          <p>My grandfather died 24 years ago.</p>
          <p>She sat on the front of the car,</p>
          <p>me and my sister on the back,</p>
          <p>so happy to ride the Marginal,</p>
          <p>“Grandma!”</p>
          <p>Said my sister. Not one to leave well enough alone.</p>
          <p>“Yes?”</p>
          <p>“Aunt Maria is dead.”</p>
          <p>…</p>
          <p>“Yes. Yes she she is … Silly of me.”</p>
          <p>She said nothing for a while,</p>
          <p>but smiled…</p>
          <p>at the road,</p>
          <p>the night,</p>
          <p>the sea</p>
          <p>the trees</p>
          <p>the houses</p>
          <p>…</p>
          <p>I don’t know.</p>
          <p>She just seemed content</p>
          <p>to see the world pass by,</p>
          <p>my father by her side,</p>
          <p>and her ghosts around her.</p>
          <p>That night I gave my Grandmother for dead.</p><br>
          <p>////</p><br>
          <p>When she died,</p>
          <p>really died,</p>
          <p>it was much more kinetic and a lot less poetic.</p>
          <p>I saw her again at a burial in Cascais cemetery,</p>
          <p>where my grandfather was buried</p>
          <p>and where she had bought a plot next to him.</p>
          <p>We were burying a son from one of her sisters,</p>
          <p>a 41 year old man,</p>
          <p>prime of his life,</p>
          <p>cancer.</p>
          <p>As the men started shoveling dirt into the hole,</p>
          <p>I became more aware of my grandmother’s discomfort.</p>
          <p>She would twitch nervously among the graves,</p>
          <p>not knowing where to be.</p>
          <p>She came next to us,</p>
          <p>Tsk tsking,</p>
          <p>as if the funeral was all wrong.</p>
          <p>I think it was by the third time she went through the grave that my mother took her by the arm and asked:</p>
          <p>“Mother, are you OK?”</p>
          <p>“Yes … Yes … it’s just that, I’m so old, and this is my graveyard … I feel that I’m next.”</p>
          <p>“Mother, don’t say that!</p>
          <p>you look so well!”</p>
          <p>My grandmother smiled thinly,</p>
          <p>shrugged her shoulders,</p>
          <p>and went near his grave.</p>
          <p>Her sister was leaving</p>
          <p>already by the exit,</p>
          <p>crying and held by someone.</p>
          <p>We followed.</p>
          <p>Behind all of us,</p>
          <p>my grandmother stood looking at my uncle’s freshly dug grave.</p>
          <p>My sister sighed as she looked at her.</p>
          <p>I said:</p>
          <p>“I’ll get her”</p>
          <p>“No …I will”.</p>
          <p>She went to her side,</p>
          <p>teetering above her nephew’s grave,</p>
          <p>and hugged her,</p>
          <p>hard.</p>
          <p>Her shoulders moved upwardly,</p>
          <p>surprised to be held,</p>
          <p>and let herself be touched.</p>
          <p>For a while,</p>
          <p>for a little while.</p>
          <p>She patted my sister,</p>
          <p>took her arm,</p>
          <p>leaned on the cane,</p>
          <p>and pointed to the exit with her chin.</p>
          <p>They moved from my uncle’s grave</p>
          <p>one cane held, the other looking at the ground,</p>
          <p>through my mother,</p>
          <p>who looked at me</p>
          <p>wet eyed and empathizing,</p>
          <p>expecting of me what this place took from everyone.</p>
          <p>But me,</p>
          <p>having no one but me to subsist,</p>
          <p>being</p>
          <p>then</p>
          <p>so bereft of others</p>
          <p>and stripped of warmth,</p>
          <p>could only think:</p>
          <p>“I must get away from here before they understand how much I don’t care.”</p>
          <p>I looked at the gravel,</p>
          <p>and hoped that my grandmother’s trek through the graveyard would be as long as it could be.</p>
          <p>The bitch.</p><br>
          <p>////</p><br>
          <p>Have I talked about her dying?</p>
          <p>Yes I did.</p>
          <p>And then I remembered the death of someone else.</p>
          <p>My mistake.</p>
          <p>I apologize.</p>
          <p>I will tell you</p>
          <p>finally,</p>
          <p>of her death.</p>
          <p>I was sleeping at my girlfriends when she woke me,</p>
          <p>shaking me softly:</p>
          <p>“Miguel , Miguel, your grandmother just died. I’m so sorry”</p>
          <p>“Who called?”</p>
          <p>“Your mother. Your father tried to phone you but you didn’t answer.”</p>
          <p>I looked at the phone</p>
          <p>It was around 3h30.</p>
          <p>My father called,</p>
          <p>for the first time,</p>
          <p>at 2h05.</p>
          <p>I wasn’t very late.</p>
          <p>I got up,</p>
          <p>took a bath,</p>
          <p>Shaved,</p>
          <p>put on my suit,</p>
          <p>a dark tie,</p>
          <p>and my black overcoat.</p>
          <p>I arrived and my father was crying.</p>
          <p>“Miguel, I’m so glad you came!</p>
          <p>please don’t go to your grandmother’s bedroom. There’s nothing to see there,</p>
          <p>not anymore…</p>
          <p>she died without her teeth and her wig.</p>
          <p>I just couldn’t let anyone see her like that.”</p>
          <p>By then all had been done.</p>
          <p>My father had welcomed the police and the 911.</p>
          <p>they did what they do,</p>
          <p>said how sorry they were for his loss</p>
          <p>and went away.</p>
          <p>Leaving him with his mother</p>
          <p>under a sheet,</p>
          <p>with her wig</p>
          <p>and her teeth.</p>
          <p>He saw me looking at her and said:</p>
          <p>“She died in my arms.</p>
          <p>I was sleeping when I heard her cry:</p>
          <p>“Zé! Zé! Come here, I can not breathe!”</p>
          <p>she was quivering uncontrollably,</p>
          <p>her nightgown at her breast,</p>
          <p>no teeth,</p>
          <p>no hair.</p>
          <p>she looked at me gasping for air.</p>
          <p>I said:</p>
          <p>“Mother, mother it will be all right!”</p>
          <p>trying to stop her jerking.</p>
          <p>but I couldn’t.</p>
          <p>I cradled her head,</p>
          <p>and squeezed her hand</p>
          <p>gripping her close to my chest.</p>
          <p>she moved, once</p>
          <p>twice,</p>
          <p>thrice,</p>
          <p>and then stopped.”</p>
          <p>There’s nothing of your grandmother to see in that bed.</p>
          <p>Just a shell”.</p>
          <p>He cried some more,</p>
          <p>and I got curious about her corpse.</p>
          <p>I moved</p>
          <p>stealthily</p>
          <p>to where she lied.</p>
          <p>She was small,</p>
          <p>she was always small,</p>
          <p>but now she was just a bump on a bed.</p>
          <p>I pulled the sheet and looked.</p>
          <p>Her jaw jutted sideways,</p>
          <p>as if it trying to leave her face.</p>
          <p>the eyes looked upward, looking intently at the ceiling.</p>
          <p>My father tried to closed them, but they opened again.</p>
          <p>Hands were clenched around her breast</p>
          <p>trying to move a weight from her chest.</p>
          <p>I saw all this and thought</p>
          <p>”God, I hope my father doesn’t see this!”</p>
          <p>But he did.</p>
          <p>Alone</p>
          <p>for a long time.</p>
          <p>He put her teeth</p>
          <p>and her wig on.</p>
          <p>As he cried it dawned on me,</p>
          <p>I can not help this man from the horror of the death of his mother.</p>
          <p>but maybe, I can help him bury her well.</p>
          <p>My grandmother now,</p>
          <p>the cunt,</p>
          <p>was so much of what she was in life,</p>
          <p>dead weight and bad memories.</p>
          <p>But maybe,</p>
          <p>just maybe,</p>
          <p>my father remembered something else,</p>
          <p>and maybe,</p>
          <p>I should respect this.</p>
          <p>So I did.</p>
          <p>The wake was a pleasant affair.</p>
          <p>I went to wash my hands and saw that I was much thinner,</p>
          <p>and that I looked good in my suit.</p>
          <p>The rest of the day I mc’ed the event,</p>
          <p>happy and self assured</p>
          <p>safe in the knowledge of my handsomeness.</p>
          <p>Some of my sisters’ girlfriends came and commented on how good I looked.</p>
          <p>It felt nice.</p>
          <p>My father cried in a corner,</p>
          <p>not able to navigate the social niceties</p>
          <p>and me so eager to do it.</p>
          <p>It was a long wake.</p>
          <p>We were there at nine in the morning and came home at midnight.</p>
          <p>All day in a tasteless chapel in one of the more nondescript places in Cascais.</p>
          <p>If I wasn’t so pleased with myself it would have been unbearable..</p>
          <p>The next day was the burial.</p>
          <p>and if you’ve been to one, I’ve nothing more to add.</p>
          <p>The same solemn steps,</p>
          <p>the same hole,</p>
          <p>that thud in your heart when you hear the dirt coming over the casket,</p>
          <p>and the lingering feeling that,</p>
          <p>all in all,</p>
          <p>better them than us.</p><br>
          <p>////</p><br>
          <p>I wish I had an ending for this poem,</p>
          <p>some kind of coda to wrap the story in a pithy summation.</p>
          <p>But I haven’t.</p>
          <p>The story is what I wrote,</p>
          <p>even if I don’t know</p>
          <p>clearly</p>
          <p>what I intended to tell.</p>
          <p>I read that some mathematical problems can’t be summed in a equation.</p>
          <p>there’s no way to discern an underlying algorithm,</p>
          <p>no way to summarize elegantly the unruly mess that are some questions.</p>
          <p>Sometimes</p>
          <p>the only way to explain them</p>
          <p>Is going through all the calculations,</p>
          <p>step by step,</p>
          <p>painstakingly forbearing any intelligence,</p>
          <p>and giving yourself,</p>
          <p>freely,</p>
          <p>to arithmetic.</p>
          <p>And I think that is what happened here.</p>
          <p>The story is the story,</p>
          <p>void of morality and gnosis.</p>
          <p>And that will have to do.</p>
